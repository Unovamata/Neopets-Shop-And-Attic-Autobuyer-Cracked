function updateDateTimeInputs(_) {
  var E = moment(_)
      .tz("America/Los_Angeles")
      .format("YYYY-MM-DD"),

    T = moment(_)
      .tz("America/Los_Angeles")
      .format("YYYY-MM-DD HH:mm:ss")
      .split(" ")[1];
  $("#ATTIC_LAST_REFRESH_DATE").val(E);
  $("#ATTIC_LAST_REFRESH_TIME").val(T)
}

function getPacificTimeDateObjFromInputs() {
  return moment.tz($("#ATTIC_LAST_REFRESH_DATE")
    .val() + " " + $("#ATTIC_LAST_REFRESH_TIME")
    .val(), "America/Los_Angeles")
    .toDate()
}

function showOrHide() {
  $("#USE_ITEM_DB").is(":checked") ? (
    $(".db-hide").show(), $(".restock-hide").hide()
  ) : (
    $(".restock-hide").show(), $(".db-hide").hide()
  );

  $("#USE_BLACKLIST").is(":checked") ? $(".blacklist-hide").show() : $(".blacklist-hide").hide();
  $("#CLICK_ITEM").is(":checked") ? $(".click-hide").show() : $(".click-hide").hide();
  $("#ATTIC_CLICK_ITEM").is(":checked") ? $(".attic-click-hide").show() : $(".attic-click-hide").hide();
  $("#ATTIC_SHOULD_REFRESH").is(":checked") ? $(".attic-refresh-hide").show() : $(".attic-refresh-hide").hide();
  $("#CLICK_CONFIRM").is(":checked") ? $(".confirm-hide").show() : $(".confirm-hide").hide();
  $("#ATTIC_ENABLED").is(":checked") ? $(".attic-settings").show() : $(".attic-settings").hide();
  $("#ENABLED").is(":checked") ? $(".main-shop-settings").show() : $(".main-shop-settings").hide();
  $("#SHOULD_ENTER_PIN").is(":checked") ? $(".enter-pin").show() : $(".enter-pin").hide();
  $("#USE_BLACKLIST_SW").is(":checked") ? $(".blacklist-sw").show() : $(".blacklist-sw").hide();
  $("#USE_BLACKLIST_KQ").is(":checked") ? $(".blacklist-kq").show() : $(".blacklist-kq").hide();
  $("#SHOULD_SUBMIT_AUTOMATICALLY").is(":checked") ? $(".submit-automatically").show() : $(".submit-automatically").hide();
  $("#SHOULD_REFRESH_THROUGH_PAGE_LOAD_FAILURES").is(":checked") ? $(".refresh-hide").show() : $(".refresh-hide").hide();
  $("#SHOULD_USE_CUSTOM_HAGGLE_MULTIPLIERS").is(":checked") ? $(".haggle-hide").show() : $(".haggle-hide").hide();

  $("#IS_TURBO").is(":checked") ? (
    $(".turbo-tag").show(), $(".turbo-hide").hide()
  ) : (
    $(".turbo-tag").hide(), $(".turbo-hide").show()
  );

  switch ($("#RESUBMIT_TYPE").val()) {
    case "Absolute":
      $(".resubmit-random").hide();
      $(".resubmit-absolute").show();
      break;

    case "Random":
      $(".resubmit-random").show();
      $(".resubmit-absolute").hide();
      break;
  }

  switch ($("#PRICING_TYPE").val()) {
    case "Absolute":
      $(".percentage").hide();
      $(".absolute").show();

      HideFixedPricingSections();
      break;

    case "Percentage":
      $(".percentage").show();
      $(".absolute").hide();

      HidePricingPercentageSections();
      break;

    default:
      $(".percentage").show();
      $(".absolute").show();

      HidePricingPercentageSections();
      HideFixedPricingSections();
      break;
  }
}

function HidePricingPercentageSections() {
  if ($("#SHOULD_USE_RANDOM_PERCENTAGES_FOR_PRICING").is(":checked")) {
    $(".random-percentage").show();
    $(".fixed-percentage").hide();
  } else {
    $(".random-percentage").hide();
    $(".fixed-percentage").show();
  }
}

function HideFixedPricingSections() {
  switch ($("#FIXED_PRICING_ALGORITHM_TYPE").val()) {
    case "True Absolute":
      $(".fixed-absolute").show();
      $(".random-absolute").hide();
      break;

    case "Random Absolute":
      $(".random-absolute").show();
      $(".fixed-absolute").hide();
      break;

    default:
      $(".random-absolute").show();
      $(".fixed-absolute").show();
      break;
  }
}

